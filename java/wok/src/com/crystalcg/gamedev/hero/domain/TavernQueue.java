package com.crystalcg.gamedev.hero.domain;

import java.util.Date;

/**
 * 酒馆job数据队列
 * @author xuzhongxing
 *
 */
public class TavernQueue {
	
	private int tavernQueueId;
	private int charId;
	private Date nextFireTime;
	private int circleTime;
	  
	public int getTavernQueueId() {
		return tavernQueueId;
	}
	public void setTavernQueueId(int tavernQueueId) {
		this.tavernQueueId = tavernQueueId;
	}
	public int getCharId() {
		return charId;
	}
	public void setCharId(int charId) {
		this.charId = charId;
	}
	public Date getNextFireTime() {
		return nextFireTime;
	}
	public void setNextFireTime(Date nextFireTime) {
		this.nextFireTime = nextFireTime;
	}
	public int getCircleTime() {
		return circleTime;
	}
	public void setCircleTime(int circleTime) {
		this.circleTime = circleTime;
	}
	


}
