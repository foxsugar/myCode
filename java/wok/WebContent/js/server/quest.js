var quest = {};
quest.getUserQuest = function(callBack){
	$.ajaxPost('getUserQuest',callBack);
};
quest.getSpecficQuest = function(questId,callBack){
	$.ajaxPost('getSpecficQuest',{questId:questId},callBack);
};
quest.submitQuest = function(questId,callBack){
	$.ajaxPost('submitQuest',{questId:questId},callBack);
};